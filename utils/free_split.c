#include "minishell.h"

void	free_split(char ***s)
{
	size_t	i;

	if (!s || !*s)
		return ;
	i = 0;
	while ((*s)[i])
	{
		free((*s)[i]);
		i++;
	}
	free(*s);
	*s = NULL;
}
